/*
	Além de usar nos Objetos: {}, você pode usar nos arrays: [].
	
	Object.keys(): extrai as chaves/índices.
	Object.values: extrai os valores das chaves/índices.
	Object.entries: extrai as keys e os values.
*/

let nossoArray = ['Zé', 'Joana', 'Ricardo'];


console.log( Object.keys(nossoArray) );

console.log( Object.values(nossoArray) );

console.log( Object.entries(nossoArray) );




